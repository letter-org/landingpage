export function Logo({ variant = "dark", showText = true, className = "", size = "md" }: LogoProps) {
  const sizeMap = {
    sm: { height: "h-6", iconHeight: "h-6 w-6" },
    md: { height: "h-8", iconHeight: "h-8 w-8" },
    lg: { height: "h-12", iconHeight: "h-12 w-12" },
  }

  const { height, iconHeight } = sizeMap[size]
  const isWhite = variant === "white"

  if (!showText) {
    return (
      <div className={`relative ${className}`}>
        <LogoIcon className={iconHeight} isWhite={isWhite} />
      </div>
    )
  }

  return (
    <div className={`flex items-center ${className}`}>
      <LogoFull className={height} isWhite={isWhite} />
    </div>
  )
}
